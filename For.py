# Percorrendo uma lista de números e imprimindo cada elemento
numeros = [1, 2, 3, 4, 5]

print("Percorrendo a lista:")
for i in numeros:
    print(i)

print("\n")

# Percorrendo um intervalo de números de 0 a 10 e imprimindo cada número
print("Percorrendo um intervalo:")
for x in range(11):
    print(x)

print("\n")

