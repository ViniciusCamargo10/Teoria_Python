# Demonstrando operações aritméticas básicas em Python

# Soma: Adiciona dois números
num1 = 10
num2 = 2
resultado_soma = num1 + num2
print(f"Soma: {resultado_soma}\n")

# Subtração: Subtrai num2 de num1
num1 = 2
num2 = 1
resultado_subtracao = num1 - num2
print(f"Subtração: {resultado_subtracao}\n")

# Multiplicação: Multiplica num1 por num2
num1 = 5
num2 = 5
resultado_multiplicacao = num1 * num2
print(f"Multiplicação: {resultado_multiplicacao}\n")

# Divisão: Divide num1 por num2
num1 = 10
num2 = 2
resultado_divisao = num1 / num2
print(f"Divisão: {resultado_divisao}\n")

# Resto: Retorna o resto da divisão (módulo)
num1 = 10
num2 = 3
resultado_resto = num1 % num2
print(f"Resto da divisão: {resultado_resto}\n")

# Exponenciação: Eleva num1 à potência de num2
num1 = 10
num2 = 2
resultado_exponenciacao = num1 ** num2
print(f"Exponenciação: {resultado_exponenciacao}")
