# Demonstrando operadores de comparação em Python

# Igualdade: Verifica se dois valores são iguais
num1 = 10
num2 = 10
resultado_igual = num1 == num2
print(f"Igualdade (10 == 10): {resultado_igual}\n")

# Diferente: Verifica se dois valores são diferentes
num1 = 10
num2 = 5
resultado_diferente = num1 != num2
print(f"Diferente (10 != 5): {resultado_diferente}\n")

# Maior: Verifica se num1 é maior que num2
num1 = 10
num2 = 5
resultado_maior = num1 > num2
print(f"Maior (10 > 5): {resultado_maior}\n")

# Maior ou igual: Verifica se num1 é maior ou igual a num2
num1 = 10
num2 = 10
resultado_maior_ou_igual = num1 >= num2
print(f"Maior ou Igual (10 >= 10): {resultado_maior_ou_igual}\n")

# Menor: Verifica se num1 é menor que num2
num1 = 5
num2 = 10
resultado_menor = num1 < num2
print(f"Menor (5 < 10): {resultado_menor}\n")

# Menor ou igual: Verifica se num1 é menor ou igual a num2
num1 = 5
num2 = 5
resultado_menor_ou_igual = num1 <= num2
print(f"Menor ou Igual (5 <= 5): {resultado_menor_ou_igual}\n")

# Exemplo extra: Verificando se dois valores são diferentes
a = 5
b = 5
print(f"5 é diferente de 5? {a != b}")
